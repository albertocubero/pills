
/**
 * Divide two numbers.
 *
 * @param {Number} a The first number in a division. Dividend.
 * @param {Number} b The second number in a division. Divisor.
 * @return {Number} Returns the quotient.
 * @example
 *
 *   divide(10, 2)  // 5
 *   divide(100, 2)  // 50
 *   divide(1000, 2)  // 500
 *   divide(6, 4)  // 1.5
 *
 **/
 
const divide = (a, b) => Number(a) / Number(b)

export default divide
